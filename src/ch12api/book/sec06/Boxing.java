package ch12api.book.sec06;

public class Boxing {
    public static void main(String[] args) {
        Integer obj = 100;
        System.out.println("value : " + obj.intValue());

        int value = 100;
        System.out.println("value = " + value);

        int result = obj + 100;
        System.out.println("result = " + result);
    }
}
